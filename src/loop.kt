fun main(args: Array<String>) {
    //FOR LOOP
    //
    println("For Loop")
    for (i in 1..4){
        println("hello")
    }

    //WHILE LOOP:
    var i = 0
    println("While Loop")
    while (i<5){
        println("Hello")
        i++
    }
    i = 0
    //DO WHILE LOOP:
    println("Do While Loop")
    do{
        println("Hello")
        i++
    } while (i<5)
}