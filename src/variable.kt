fun main(args: Array<String>) {
    //mutable variable
    var myNumber =10
    var myDecimal = 1.0
    var myString:String
    myString = "Hello Dude"
    myString = "Hello Noob"

    //immutable variable
    val myAnotherString :String
    myAnotherString = "Hello This is immutable var"

    println("Number "+ myNumber)
    println("Decimal "+ myDecimal)
    println("String Mutable "+ myString)
    println("String Immutable "+ myAnotherString)

}